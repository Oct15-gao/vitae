$(document).ready(function() {
    $('.search-icon').click(function(event) {
        $('.search-input').fadeToggle(500);
    });

    $('.signPage.UP')
    $('.signPage.IN')

    $('.sign-in a').click(function(event) {
        $('.signPage.IN').fadeIn(300)
    });
    $('.sign-up a').click(function(event) {
        $('.signPage.UP').fadeIn(300)
    });
    $('.close').click(function(event) {
        $('.signPage').fadeOut(300)
    });












});
